	<h2 class="post_title">The Issues at Hand</h2>
	<p>
		Hon. Bishop Dr. Margaret Wanjiru was born in Nairobi as the third child into a very poor family of casual workers. Despite the many challenges that come with poverty, her hard working mother was able to put her through both primary and high school while instilling the discipline necessary to excel in life.</p><p>
She worked hard in her pursuit of a better future and by the time she was 17 years old, she was paying her way through college by working numerous jobs. She worked as a house girl, hawker, a toilet cleaner, car cleaner and office cleaner. On graduation from college she earned herself a position as sales girl.</p><p>
This discipline and determination to succeed resulted in rapid promotions and various accomplishments until she become a sales and marketing executive for a multinational firm in the city. She later went on to form her own business in the city excelling both as an entrepreneur and single mother to her two sons and one daughter.
	</p>
	<h2>Today</h2>
	<p>

Later on in life she gave her life to Jesus Christ and become a born again Christian. Thanks to her determination to serve the Lord Jesus Christ and great leadership skills, she is the founder and presiding Bishop of the Jesus Is Alive Ministries (JIAM), an international Christian organization with headquarter in Nairobi Kenya.</p><p>
Today, with one of the fastest growing churches in the continent of Africa, JIAM is becoming a ministry to the world with offices in many nations, touching many hearts and changing many lives through its various departments.</p><p>
Hon. Bishop Dr. Margaret Wanjiru has been used mightily by God. She oversees a massive congregation and her dynamic ministry has transformed nations especially through the media television program entitled 'The Glory Is Here'.
She is an author of many Christian books and also oversees a monthly magazine, the ‘Faith digest'.
	</p>
	<h2>National Leadership</h2>
	<p>

Hon. Bishop Dr. Margaret Wanjiru has for a long time been actively involved with matters of national concern. As a leader she has for years been a vocal participant in governmental policy and leadership issues across the board.</p><p>
Politicians have for years called on the Kenyan churches when elections near on a vote seeking mission only to soon after renege on their every promise to the people of Kenya . Her call to national leadership (politics) is as a result of the people demanding for better leadership and representation in government.</p><p>
In 2007, she was elected as a Member of Parliament for Starehe Constituency in a hotly contested election in one of the most challenging constituencies in Nairobi the capital city of Kenya.</p><p>
She now has the opportunity to not only continue pursuing better governance for the Kenyan people but to also be a key participant in the policy making and implementation process as we work together towards a better Kenya.</p><p>
In 2008, she was appointed into the cabinet as an Assistant Minister for Housing.
She has a Doctorate in Divinity excelling in the matters of religion, education and human sciences. Recently, she obtained a certificate on governance from Harvard University - USA.
	</p>
	
